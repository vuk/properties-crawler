function buildLocationAliasEntries(): [string, SerbianMunicipality][] {
    const merged = [...LOCATION_ALIAS_RAW, ...LOCATION_ALIAS_EXTRA];
    const seen = new Map<string, SerbianMunicipality>();
    for (const [raw, v] of merged) {
        const key = foldSerbianLatin(raw).replace(/\s+/g, ' ').trim();
        if (!key) continue;
        if (!seen.has(key)) seen.set(key, v);
    }
    return [...seen.entries()]
        .sort((a, b) => b[0].length - a[0].length || a[0].localeCompare(b[0]))
        .map(([k, v]) => [k, v] as [string, SerbianMunicipality]);
}

const LOCATION_ALIAS_ENTRIES: [string, SerbianMunicipality][] = buildLocationAliasEntries();

function matchesLocationAlias(folded: string, alias: string): boolean {
    const a = alias.trim();
    if (!a) return false;
    if (a.includes(' ')) return folded.includes(a);
    const re = new RegExp(`(^|[^a-z0-9])${escapeRegExp(a)}([^a-z0-9]|$)`);
    return re.test(folded);
}

/**
 * Resolve a municipality/city from free text, URL slug, or combined hints.
 * Longer alias matches win; single-token aliases use non-alphanumeric boundaries.
 */
export function resolveSerbianMunicipality(haystack: string): SerbianMunicipality {
    if (!haystack) return SerbianMunicipality.UNKNOWN;
    const folded = foldSerbianLatin(haystack).replace(/[_-]+/g, ' ');
    for (const [alias, value] of LOCATION_ALIAS_ENTRIES) {
        if (matchesLocationAlias(folded, alias)) return value;
    }
    return SerbianMunicipality.UNKNOWN;
}
