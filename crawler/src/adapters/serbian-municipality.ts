function escapeRegExp(s: string): string {
    return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

/**
 * Normalize Serbian Latin for matching: đ→dj, dž/lj/nj digraphs, strip č/ć/š/ž marks, lowercase.
 */
export function foldSerbianLatin(s: string): string {
    let t = s
        .replace(/\u01C4/g, 'Dz')
        .replace(/\u01C5/g, 'Dz')
        .replace(/\u01C6/g, 'dz')
        .replace(/\u01F1/g, 'Dz')
        .replace(/\u01F2/g, 'Dz')
        .replace(/\u01F3/g, 'dz')
        .replace(/\u01CA/g, 'Nj')
        .replace(/\u01CB/g, 'Nj')
        .replace(/\u01CC/g, 'nj')
        .replace(/\u01C7/g, 'Lj')
        .replace(/\u01C8/g, 'Lj')
        .replace(/\u01C9/g, 'lj');
    return t
        .replace(/DŽ/g, 'Dz')
        .replace(/Dž/g, 'Dz')
        .replace(/dž/g, 'dz')
        .replace(/Đ/g, 'Dj')
        .replace(/đ/g, 'dj')
        .normalize('NFD')
        .replace(/\p{M}/gu, '')
        .toLowerCase();
}

